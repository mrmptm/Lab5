void command_parser_fsm(){
	switch(state){
	case INIT:
		if(temp=='!'){
			state=READ_START;
		}
		break;
	case READ_START:
		if(temp=='R'){
			state=READ_R;
		}else if(temp=='O'){
			state=READ_O;
		}else if(temp=='!'){state=READ_START;}
		else state=INIT;
		break;
	case READ_R:
		if(temp=='S'){
			state=READ_S;
		}else if(temp=='!'){state=READ_START;}
		else state=INIT;
		break;
	case READ_S:
		if(temp=='T'){
			state=READ_T;
		}else if(temp=='!'){state=READ_START;}
		else state=INIT;
		break;
	case READ_O:
		if(temp=='K'){
			state=READ_K;
		}else if(temp=='!'){state=READ_START;}
		else state=INIT;
		break;
	case READ_K:
		if(temp=='#'){
			command_flag=1;
			command_data=OK;
			state=INIT;
		}else if(temp=='!'){
			state=READ_START;
		}else state=INIT;
		break;
	case READ_T:
		if(temp=='#'){
			timer1_flag=1;
			command_flag=1;
			command_data=RST;
			state=INIT;
		}else if(temp=='!'){
			state=READ_START;
		}else state=INIT;
		break;
	}
//	HAL_UART_Transmit(&huart2, str, sprintf(str,"STATE:%d "
//			"TEMP:%c flag:%d data:%d\r",state,temp,command_flag,command_data), 100);
}
int analog_Read=0;
void readAnalog(){
	HAL_ADC_Start(&hadc1);
	HAL_ADC_PollForConversion(&hadc1,1);
	value=HAL_ADC_GetValue(&hadc1);
	HAL_ADC_Stop(&hadc1);
}
void uart_communication_fsm(){
	if(command_flag==1){
		switch(command_data){
		case RST:
			if(timer1_flag==1){
				setTimer1(3000);
				if(analog_Read==0){
					readAnalog();
					analog_Read=1;
					previous_packet=value;
				}
				HAL_UART_Transmit(&huart2, str, sprintf(str,"!ADC=%d#\r\n",previous_packet), 100);
			}
			break;
		case OK:
			command_flag=0;
			analog_Read=0;
			break;
		}
	}
}
